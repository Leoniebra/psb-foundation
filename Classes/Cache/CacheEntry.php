namespace PSB\PsbFoundation\Cache;

use PSB\PsbFoundation\Domain\Model\AbstractModelWithDataManipulationProtection;

/**
 * Class CacheEntry
 * @package PSB\PsbFoundation\Cache
 */
class CacheEntry extends AbstractModelWithDataManipulationProtection
{
    /**
     * @var string
     */
    protected string $content;

    /**
     * @var string
     */
    protected string $identifier;

    /**
     * @return string
     */
    public function getContent(): string
    {
        return $this->content;
    }

    /**
     * @param string $content
     */
    public function setContent(string $content): void
    {
        $this->content = $content;
    }

    /**
     * @return string
     */
    public function getIdentifier(): string
    {
        return $this->identifier;
    }

    /**
     * @param string $identifier
     */
    public function setIdentifier(string $identifier): void
    {
        $this->identifier = $identifier;
    }
}
